module.exports = function init(site) {
  const $lawsuit_degrees = site.connectCollection("lawsuit_degrees")

  site.get({
    name: "lawsuit_degrees",
    path: __dirname + "/site_files/html/index.html",
    parser: "html",
    compress: true
  })

  site.get({
    name: 'images',
    path: __dirname + '/site_files/images/'
  })

  site.on('[company][created]', doc => {

    $lawsuit_degrees.add({
      name_Ar: "درجة دعوى إفتراضية",
      name_En : "Default Lawsuit Degree",
      code: "1-Test",
      image_url: '/images/lawsuit_degrees.png',
      company: {
        id: doc.id,
        name_Ar: doc.name_Ar,
        name_En: doc.name_En
      },
      branch: {
        code: doc.branch_list[0].code,
        name_Ar: doc.branch_list[0].name_Ar,
        name_En: doc.branch_list[0].name_En
      },
      active: true
    }, (err, doc) => { })
  })


  site.post("/api/lawsuit_degrees/add", (req, res) => {
    let response = {}
    response.done = false

    if (!req.session.user) {
      response.error = 'Please Login First';
      res.json(response)
      return
    }

    let lawsuit_degrees_doc = req.body
    lawsuit_degrees_doc.$req = req
    lawsuit_degrees_doc.$res = res
    lawsuit_degrees_doc.add_user_info = site.security.getUserFinger({
      $req: req,
      $res: res
    })
    lawsuit_degrees_doc.company = site.get_company(req)
    lawsuit_degrees_doc.branch = site.get_branch(req)

    let num_obj = {
      company: site.get_company(req),
      screen: 'lawsuit_degrees',
      date: new Date()
    };
    let cb = site.getNumbering(num_obj);

    if (!lawsuit_degrees_doc.code && !cb.auto) {
      response.error = 'Must Enter Code';
      res.json(response);
      return;

    } else if (cb.auto) {
      lawsuit_degrees_doc.code = cb.code;
    }


    $lawsuit_degrees.add(lawsuit_degrees_doc, (err, doc) => {
      if (!err) {
        response.done = true
        response.doc = doc
      } else {
        response.error = err.message
      }
      res.json(response)
    })
  })

  site.post("/api/lawsuit_degrees/update", (req, res) => {
    let response = {
      done: false
    }

    if (!req.session.user) {
      response.error = 'Please Login First'
      res.json(response)
      return
    }

    let lawsuit_degrees_doc = req.body
    lawsuit_degrees_doc.edit_user_info = site.security.getUserFinger({
      $req: req,
      $res: res
    })
    if (lawsuit_degrees_doc.id) {
      $lawsuit_degrees.edit({
        where: {
          id: lawsuit_degrees_doc.id
        },
        set: lawsuit_degrees_doc,
        $req: req,
        $req: req,
        $res: res
      }, (err, result) => {
        if (!err) {
          response.done = true
          response.doc = result.doc
        } else {
          response.error = err.message
        }
        res.json(response)
      })
    } else {
      response.error = 'no id'
      res.json(response)
    }
  })

  site.post("/api/lawsuit_degrees/view", (req, res) => {
    let response = {
      done: false
    }

    if (!req.session.user) {
      response.error = 'Please Login First'
      res.json(response)
      return
    }

    $lawsuit_degrees.findOne({
      where: {
        id: req.body.id
      }
    }, (err, doc) => {
      if (!err) {
        response.done = true
        response.doc = doc
      } else {
        response.error = err.message
      }
      res.json(response)
    })
  })

  site.post("/api/lawsuit_degrees/delete", (req, res) => {
    let response = {
      done: false
    }

    if (!req.session.user) {
      response.error = 'Please Login First'
      res.json(response)
      return
    }

    let id = req.body.id

    if (id) {
      $lawsuit_degrees.delete({
        id: id,
        $req: req,
        $res: res
      }, (err, result) => {
        if (!err) {
          response.done = true
          response.doc = result.doc
        } else {
          response.error = err.message
        }
        res.json(response)
      })
    } else {
      response.error = 'no id'
      res.json(response)
    }
  })

  site.post("/api/lawsuit_degrees/all", (req, res) => {
    let response = {
      done: false
    }

    if (!req.session.user) {
      response.error = 'Please Login First'
      res.json(response)
      return
    }

    let where = req.data.where || {}

    if (where['code']) {
      where['code'] = site.get_RegExp(where['code'], 'i')
    }

    if (where['name']) {
      where['name'] = site.get_RegExp(where['name'], 'i')
    }

    // if (where['active'] !== 'all') {
    //   where['active'] = true
    // } else {
    //   delete where['active']
    // }

    where['company.id'] = site.get_company(req).id
    where['branch.code'] = site.get_branch(req).code

    $lawsuit_degrees.findMany({
      select: req.body.select || {},
      where: where,
      sort: req.body.sort || { id: -1 },
      limit: req.body.limit
    }, (err, docs, count) => {
      if (!err) {
        response.done = true
        response.list = docs
        response.count = count
      } else {
        response.error = err.message
      }
      res.json(response)
    })
  })

}